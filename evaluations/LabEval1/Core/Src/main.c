/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_USART1_UART_Init();

  /* Initial State: LED OFF */
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);

  while (1)
  {
    /* Read button PC13 (usually active LOW on STM32 boards) */
    uint8_t button_state = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);

    /* Detect button press edge (HIGH -> LOW) */
    if (last_button_state == GPIO_PIN_SET && button_state == GPIO_PIN_RESET)
    {
      system_active = !system_active;   // Toggle system state
      HAL_Delay(200);                   // Debounce delay
    }
    last_button_state = button_state;

    if (system_active)
    {
      /* ACTIVE STATE: Blink LED at 1 Hz */

      /* LED ON */
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);
      HAL_Delay(500);

      /* LED OFF */
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);
      HAL_Delay(500);

      /* Periodic UART message */
      HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), 500);
    }
    else
    {
      /* IDLE STATE: LED OFF, no UART transmission */
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);
    }
  }
}

